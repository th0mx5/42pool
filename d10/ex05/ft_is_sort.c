int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (i < length - 1)
	{
		if ((*f)(tab[i], tab[i + 1]) < 0)
			j++;
		if ((*f)(tab[i], tab[i + 1]) > 0)
			k++;
		i++;
	}
	if (k != 0 && j != 0)
		return (0);
	return (1);
}
